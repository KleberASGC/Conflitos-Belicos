package ConflitosBelicos;

public class Pais {
	private String pais;
	private int nConfRelig;
	public Pais(String pais, int nConfRelig) {
		super();
		this.pais = pais;
		this.nConfRelig = nConfRelig;
	}
	public String getPais() {
		return pais;
	}
	public int getnConfRelig() {
		return nConfRelig;
	}
	
	
}
